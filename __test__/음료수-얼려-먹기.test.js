const solution = require("../coding-test-for-finding-jobs/음료수-얼려-먹기");

test("00110\n00011\n11111\n00000\n아이스크림: 3개", () => {
  expect(solution(4, 5, "00110 00011 11111 00000")).toBe(3);
});

